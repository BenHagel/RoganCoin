function opz_opz_newOpzProblem(xx, yy){
	let op = {};
	op.x = xx;
	op.y = yy;
	op.solvedNodes = 0;
	op.colourTargetVariance = 60;
	op.animationCounter0 = 0;
	op.animationCounter1 = 0;
	op.animationCounter2 = 0;
	////////////////////////
	op.nodes = [];
	let newNode = function(xx, yy){return {"x": xx, "y": yy, 
		"r": npowRandom.random()*255, 
		"g": npowRandom.random()*255, 
		"b": npowRandom.random()*255,
		"ra": npow_opz_colours[0].r + npowRandom.random()*op.colourTargetVariance - op.colourTargetVariance/2, 
		"ga": npow_opz_colours[0].g + npowRandom.random()*op.colourTargetVariance - op.colourTargetVariance/2, 
		"ba": npow_opz_colours[0].b + npowRandom.random()*op.colourTargetVariance - op.colourTargetVariance/2,
		"nc": 0
	};}
	for(let i = 0;i < 3;i++) op.nodes.push(newNode(-105 + i*70 + 35, -60));
	for(let i = 0;i < 4;i++) op.nodes.push(newNode(-140 + i*70 + 35, 0));
	for(let i = 0;i < 3;i++) op.nodes.push(newNode(-105 + i*70 + 35, 60));
	////////////////////////
	op.edges = [];
	let newEdge = function(a, b){return {"a": a, "b":b, "c": npowRandom.random()*255};}
	for(let i = 0;i < 3;i++) for(let j = 0;j < 4;j++)
		op.edges.push(newEdge(op.nodes[i], op.nodes[3+j]));
	for(let i = 0;i < 4;i++) for(let j = 0;j < 3;j++)
		op.edges.push(newEdge(op.nodes[3+i], op.nodes[3+4+j]));
	return op;
}




